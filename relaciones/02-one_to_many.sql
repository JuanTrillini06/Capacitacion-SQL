-- UNO A VARIOS
/* Una relación uno a varios significa que una fila en la tabla A puede estar relacionada con múltiples filas en la tabla B,
   pero una fila en la tabla B solo puede estar relacionada con una fila en la tabla A.
   Esto se logra mediante el uso de claves primarias y foráneas.
   */
/* Por ejemplo, una tabla de autores y una tabla de libros, cada autor puede haber escrito varios libros,
   pero cada libro solo tiene un autor.
   */